class LocalizerAlexNet(nn.Module):
    def __init__(self, num_classes=14):
        super(LocalizerAlexNet, self).__init__()

        # TODO: Define model
        '''
          (features): Sequential(
            (0): Conv2d(3, 64, kernel_size=(11, 11), stride=(4, 4), padding=(2, 2))
            (1): ReLU(inplace)
            (2): MaxPool2d(kernel_size=(3, 3), stride=(2, 2), dilation=(1, 1), ceil_mode=False)
            (3): Conv2d(64, 192, kernel_size=(5, 5), stride=(1, 1), padding=(2, 2))
            (4): ReLU(inplace)
            (5): MaxPool2d(kernel_size=(3, 3), stride=(2, 2), dilation=(1, 1), ceil_mode=False)
            (6): Conv2d(192, 384, kernel_size=(3, 3), stride=(1, 1), padding=(1, 1))
            (7): ReLU(inplace)
            (8): Conv2d(384, 256, kernel_size=(3, 3), stride=(1, 1), padding=(1, 1))
            (9): ReLU(inplace)
            (10): Conv2d(256, 256, kernel_size=(3, 3), stride=(1, 1), padding=(1, 1))
            (11): ReLU(inplace)
          )
        '''
        self.features = nn.Sequential(
            nn.Conv2d(in_channels=3, out_channels=64, kernel_size=(11, 11), stride=(4, 4), padding=(2, 2)),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(kernel_size=(3, 3), stride=(2, 2), dilation=(1, 1), ceil_mode=False),
            nn.Conv2d(in_channels=64, out_channels=192, kernel_size=(5, 5), stride=(1, 1), padding=(2, 2)),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(kernel_size=(3, 3), stride=(2, 2), dilation=(1, 1), ceil_mode=False),
            nn.Conv2d(in_channels=192, out_channels=384, kernel_size=(3, 3), stride=(1, 1), padding=(1, 1)),
            nn.ReLU(inplace=True),
            nn.Conv2d(in_channels=384, out_channels=256, kernel_size=(3, 3), stride=(1, 1), padding=(1, 1)),
            nn.ReLU(inplace=True),
            nn.Conv2d(in_channels=256, out_channels=256, kernel_size=(3, 3), stride=(1, 1), padding=(1, 1)),
            nn.ReLU(inplace=True)
        )

        '''
          (classifier): Sequential(
            (0): Conv2d(256, 256, kernel_size=(3, 3), stride=(1, 1))
            (1): ReLU(inplace)
            (2): Conv2d(256, 256, kernel_size=(1, 1), stride=(1, 1))
            (3): ReLU(inplace)
            (4): Conv2d(256, 20, kernel_size=(1, 1), stride=(1, 1))
          )
        '''
        self.classifier = nn.Sequential(
            nn.Conv2d(in_channels=256, out_channels=256, kernel_size=(3, 3), stride=(1, 1)),
            nn.ReLU(inplace=True),
            nn.Conv2d(in_channels=256, out_channels=256, kernel_size=(1, 1), stride=(1, 1)),
            nn.ReLU(inplace=True),
            nn.Conv2d(in_channels=256, out_channels=num_classes, kernel_size=(1, 1), stride=(1, 1))
        )

    def forward(self, x):
        # Define forward pass
        x = self.features(x)
        x = self.classifier(x)
        return x


def localizer_alexnet(pretrained=False, **kwargs):
    r"""AlexNet model architecture from the
    `"One weird trick..." <https://arxiv.org/abs/1404.5997>`_ paper.

    Args:
        pretrained (bool): If True, returns a model pre-trained on ImageNet
    """
    model = LocalizerAlexNet(**kwargs)

    if pretrained is True:
        # load parts of pre-train
        state_dict_pretrain = model_zoo.load_url(model_urls['alexnet'])
        state_dict_new = {}
        for item_name, item_value in state_dict_pretrain.iteritems():
            # load the pre-train for the first 5 layers
            if 'features' in item_name:
                state_dict_new[item_name] = item_value

        model_dict = model.state_dict()
        model_dict.update(state_dict_new)
        model.load_state_dict(model_dict)

        # Fix weights in Features part
        for para in model.features.parameters():
            para.requires_grad = True
        for para in model.classifier.parameters():
            para.requires_grad = True

    return model


